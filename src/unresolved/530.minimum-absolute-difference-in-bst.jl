# Given a binary search tree with non-negative values, find the minimum
# [absolute difference](https://en.wikipedia.org/wiki/Absolute_difference)
# between values of any two nodes.
# 
# **Example:**
# 
#     
#     
#     Input:
#     
#        1
#         \
#          3
#         /
#        2
#     
#     Output:
#     1
#     
#     Explanation:
#     The minimum absolute difference is 1, which is the difference between 2 and 1 (or between 2 and 3).
#     
# 
# 
# 
# **Note:**
# 
#   * There are at least two nodes in this BST.
#   * This question is the same as 783: <https://leetcode.comhttps://leetcode.com/problems/minimum-distance-between-bst-nodes/>
