@testset "190.reverse-bits.jl" begin
    @test reverse_bit(0b00000010100101000001111010011100) ==
          0b00111001011110000010100101000000
    @test reverse_bit(0b11111111111111111111111111111101) ==
          0b10111111111111111111111111111111
    @test reverse_bit(0b00000010100101000001111010011100) ==
          0b00111001011110000010100101000000
    @test reverse_bit(0b11111111111111111111111111111101) ==
          0b10111111111111111111111111111111
end
